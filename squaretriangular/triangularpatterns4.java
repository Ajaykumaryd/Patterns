package squaretriangular;
import java.util.Scanner;
public class triangularpatterns4 {
	public static void main(String[]args){
//		1
//		21
//		321
//		4321
//		54321
		
		Scanner s=new Scanner(System.in);
		int n=s.nextInt();
		
		int i=1;
		while(i<=n){			int j=i;
			while(j>=1){
				System.out.print(j);
				j--;}
               System.out.println();
               i++;}}
				
	        
	          
}
