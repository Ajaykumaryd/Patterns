package patterns;

public class patterns3plus2 {

	public static void main(String[] args) {
		for(int i=1;i<=4;i++){
			for(int w=1;w<=i;w++){
			System.out.print("*");}
			System.out.println();}
		for(int i=1;i<=4;i++){
			for(int j=4;j>=i;j--){
				System.out.print("*");}
	            System.out.println();  		
}
}
}
